// 聊天接口：前端发消息到这里，我们再转发给大模型
app.post('/chat', async (req, res) => {
  // 拿到前端发来的用户消息
  const { userMsg } = req.body
  try {
    // 1. 把用户的新消息加入历史记录
    historyMessages.push({ role: "user", content: userMsg })
    // 2. 向大模型发送请求，带上系统人设 + 完整历史对话
    const response = await axios.post(
      AI_API_URL,
      {
        model: AI_MODEL_NAME,
        messages: [
          // 固定人设：永远放在第一条
          { role: "system", content: ALIYA_SYSTEM_PROMPT },
          // 展开全部历史对话（包含刚才的用户新消息）
          ...historyMessages
        ],
        temperature: 0.7
      },
      {
        headers: {
          "Content-Type": "application/json",
          "Authorization": `Bearer ${AI_API_KEY}`
        }
      }
    )
    // 提取AI回复
    let aiReply = response.data.choices[0].message.content
    // 强制过滤所有括号及括号内的内容，彻底清除动作/语气/场景描写
    aiReply = aiReply.replace(/[（(][^）)]*[）)]/g, '').trim()
    // 3. 把AI的回复也加入历史，保证上下文完整
    historyMessages.push({ role: "assistant", content: aiReply })
    // 返回给前端
    res.json({ reply: aiReply })
  } catch (error) {
    console.error("调用大模型出错：", error.message)
    res.json({ reply: "通讯信号有点不稳定……等一下再试试好吗？" })
  }
})

